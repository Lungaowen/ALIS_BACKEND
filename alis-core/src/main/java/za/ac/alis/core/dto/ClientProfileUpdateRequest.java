package za.ac.alis.core.dto;

public class ClientProfileUpdateRequest {

    private String fullName;
    private String username;
    private String currentPassword;
    private String newPassword;
    private String barNumber;
    private String lawFirm;
    private String companyName;
    private String dealSpecialty;

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getCurrentPassword() {
        return currentPassword;
    }

    public void setCurrentPassword(String currentPassword) {
        this.currentPassword = currentPassword;
    }

    public String getNewPassword() {
        return newPassword;
    }

    public void setNewPassword(String newPassword) {
        this.newPassword = newPassword;
    }

    public String getBarNumber() {
        return barNumber;
    }

    public void setBarNumber(String barNumber) {
        this.barNumber = barNumber;
    }

    public String getLawFirm() {
        return lawFirm;
    }

    public void setLawFirm(String lawFirm) {
        this.lawFirm = lawFirm;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getDealSpecialty() {
        return dealSpecialty;
    }

    public void setDealSpecialty(String dealSpecialty) {
        this.dealSpecialty = dealSpecialty;
    }
}
